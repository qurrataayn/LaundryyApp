package com.example.laundryyy;

public class UserInformation {
    public String nama;
    public String notelp;
    public String jenpak;
    public String tglkel;
    public String tglmas;
    public String jencuci;
    public String layanan;

    public UserInformation (String nama, String notelp,String jenpak, String tglkel, String tglmas, String jencuci, String layanan){
        this.nama = nama;
        this.notelp = notelp;
        this.jenpak = jenpak;
        this.tglkel = tglkel;
        this.tglmas = tglmas;
        this.jencuci = jencuci;
        this.layanan = layanan;
    }
}
